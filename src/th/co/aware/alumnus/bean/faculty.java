package th.co.aware.alumnus.bean;

public class faculty {
	private int f_id ;
	private String f_faculty ;
	private String major ;
	private String graduate ;
	private String graduate_start ;
	
	
	
	
	
	public int getF_id() {
		return f_id;
	}
	public void setF_id(int f_id) {
		this.f_id = f_id;
	}
	public String getF_faculty() {
		return f_faculty;
	}
	public void setF_faculty(String f_faculty) {
		this.f_faculty = f_faculty;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getGraduate() {
		return graduate;
	}
	public void setGraduate(String graduate) {
		this.graduate = graduate;
	}
	public String getGraduate_start() {
		return graduate_start;
	}
	public void setGraduate_start(String graduate_start) {
		this.graduate_start = graduate_start;
	}
	
	
		


}
